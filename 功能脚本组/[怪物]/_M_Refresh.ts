/**
 * 刷新怪物属性
 * @param Envir 环境对象
 * @param Monster 怪物对象
 * @param Tag 标签编号
 */
export function Refresh(Envir: TEnvirnoment, Monster: TActor, Tag: number): void {
    Monster.SetDenyAutoAddHP(false) // 设置自动回血

    // 初始化怪物基础属性
    const 取尾数: number = Tag % 10;
    let 怪物类别 = '';
    let 怪物的颜色 = 0;
    let 怪物后缀 = '';
    let 星星增加 = 1;
    let 怪物变色 = 255;
    let 攻速移速 = 1400;
    let 攻击小 = `0`;
    let 攻击大 = `0`;
    let 血量小 = `0`;
    let 血量大 = `0`;
    let 经验值 = 0;

    let 怪物标识 = 0;

    const 当前地图ID = Monster.Map.MapID;
    const 当前地图名 = Envir.GetName();
    
    // 添加基础调试信息
    const 当前怪物名称 = Monster.GetName();
    // console.log(`[属性调试] 怪物: ${当前怪物名称}, 地图名: ${当前地图名}, 地图ID: ${当前地图ID}, TAG: ${取尾数}`);

    // 获取当前地图配置
    const 副本池 = 地图.取地图;
    const 当前副本配置 = 副本池().find(item => item.地图ID === 当前地图ID);
    
    let 有效副本配置 = 当前副本配置;
    let 地图星级 = '1';
    
    // 检查是否是圣墟怪物
    const 是圣墟怪物 = 当前怪物名称.includes('圣墟') || 取尾数 === 5;
    
    if (!当前副本配置) {
        if (是圣墟怪物) {
            有效副本配置 = {
                地图ID: 当前地图ID,
                地图名: '圣墟',
                显示名: '圣墟(1星)',
                固定星级: 1,
                需求等级: 1,
                挑战倍数: 1,
                下标: 0,
                地图等级: 1
            } as any;
            地图星级 = '1';
        } else {
            return;
        }
    } else {
        地图星级 = 当前副本配置.显示名.match(/\((\d+)星\)/)?.[1] || '1';
    }

    // 根据副本配置查找刷怪配置
    const 当前刷怪配置 = 刷怪属性.filter(配置 => 配置.地图名字 === 有效副本配置.地图名);
    // console.log(`[属性调试] 地图 ${有效副本配置.地图名} 找到 ${当前刷怪配置.length} 个刷怪配置`);

    // 查找匹配的怪物配置
    const 匹配配置 = 当前刷怪配置.find(
        循环 => 循环.地图名字 === 有效副本配置.地图名 && Monster.GetName() === 循环.怪物名字
    );
    
    if (!匹配配置) {
        // console.log(`[属性调试] 未找到匹配配置 - 怪物: ${当前怪物名称}, 地图: ${有效副本配置.地图名}`);
        return;
    }
    
    // console.log(`[属性调试] 找到匹配配置 - 怪物: ${匹配配置.怪物名字}, 星星: ${匹配配置.怪物星星大}`);
    
    // 根据取尾数选择怪物类型(限制为1-5)
    if (取尾数 < 1 || 取尾数 > 5) return;
    const 怪物类型配置 = 生物分类[取尾数];
    if (!怪物类型配置) return;
    
    // 随机选择怪物等级
    const 是高级怪物 = 怪物类型配置.length > 1 && random(100) < 20;
    const 选定配置 = 是高级怪物 ? 怪物类型配置[0] : 怪物类型配置[怪物类型配置.length - 1];
    
    // 应用基础配置
    怪物类别 = 选定配置.类别;
    怪物的颜色 = 选定配置.颜色;
    攻速移速 = 选定配置.速度;
    攻击小 = 选定配置.攻击L;
    攻击大 = 选定配置.攻击M;
    血量小 = 选定配置.血量L;
    血量大 = 选定配置.血量M;
    经验值 = 匹配配置.经验值 * 选定配置.exp;
    怪物标识 = Tag;

    
    // 应用特殊属性
    if (选定配置.range) Monster.SetAttackRange(选定配置.range);
    if (选定配置.triggerMagic) Monster.SetTriggerSelectMagicBeforeAttack(true);
    
    // 应用地图和怪物变异效果
    const 变异结果 = 应用怪物变异(Envir, 取尾数, 当前地图名);
    怪物后缀 = 变异结果.怪物后缀;
    攻击小 = js_number(攻击小, 变异结果.攻击倍率, 3);
    攻击大 = js_number(攻击大, 变异结果.攻击倍率, 3);
    血量小 = js_number(血量小, 变异结果.血量倍率, 3);
    血量大 = js_number(血量大, 变异结果.血量倍率, 3);
    星星增加 = 变异结果.星星增加;
    怪物变色 = 变异结果.怪物变色;
    
   // 计算怪物星级
    let 怪怪星星 = 计算怪物星级(取尾数, 匹配配置, 当前地图名, 星星增加, 地图星级);
    
    // 检查是否是圣墟怪物，如果是则使用预设的星数
    
    if (是圣墟怪物 && GameLib.R.圣墟当前副本信息) {
        const 副本信息 = GameLib.R.圣墟当前副本信息 as any;
        if (副本信息.地图ID === 当前地图ID) {
            怪怪星星 = 副本信息.星星;
        }
    }
    
    Monster.SetSVar(怪物星数, String(怪怪星星));
    
    // 计算怪物防御
    let 怪物防御 = 计算怪物防御(怪怪星星, 怪物类别, 当前地图名);
    let 星星变量 = '星★';
    
    // 计算最终血量和攻击
    let 血量 = 计算最终血量(取尾数, 怪怪星星, 血量小, 血量大, 当前地图名);
    let 攻击计算小 = 计算最终攻击(怪怪星星, 攻击小, 当前地图名);
    let 攻击计算大 = 计算最终攻击(怪怪星星, 攻击大, 当前地图名);
    // 防御大 = 攻击大*0.3 防御小=攻击小*0.3
    let 物理防御小 = js_number(攻击计算小, `0.1`, 3);
    let 物理防御大 = js_number(攻击计算大, `0.1`, 3);
    
    // 特殊处理
    if (js_war(怪物防御, `1`) < 0) {
        怪物防御 = `1`;
    }
    
    
    // 设置怪物属性
    设置怪物属性(Monster, 匹配配置, 怪怪星星, 星星变量, 怪物后缀, 怪物类别, 怪物的颜色, 怪物变色, 
                攻速移速, 经验值, 血量, 攻击计算小, 攻击计算大, 物理防御小, 物理防御大, 地图星级, 怪物标识);
}

/**
 * 应用怪物变异效果
 * @param Envir 环境对象
 * @param 取尾数 怪物标签尾数
 * @param 当前地图名 当前地图名称
 * @returns 变异后的属性对象
 */
function 应用怪物变异(Envir: TEnvirnoment, 取尾数: number, 当前地图名: string): {怪物后缀: string, 攻击倍率: string, 血量倍率: string, 星星增加: number, 怪物变色: number} {
    const 结果 = {
        怪物后缀: '',
        攻击倍率: '1',
        血量倍率: '1',
        星星增加: 1,
        怪物变色: 255
    };
    

    // 随机变异几率
    const 几率 = random(1000);
    
    // 应用变异规则
    if (几率 >= 0 && 几率 <= 10) {
        // 魔鬼变异 (0-10)
        结果.怪物后缀 = 怪物变异配置.魔鬼.后缀;
        结果.攻击倍率 = String(怪物变异配置.魔鬼.攻击倍率());
        结果.血量倍率 = String(怪物变异配置.魔鬼.血量倍率());
        结果.怪物变色 = 怪物变异配置.魔鬼.颜色;
    } else if (几率 >= 40 && 几率 <= 80) {
        // 暗黑变异 (40-80)
        结果.怪物后缀 = 怪物变异配置.暗黑.后缀;
        结果.攻击倍率 = String(怪物变异配置.暗黑.攻击倍率());
        结果.血量倍率 = String(怪物变异配置.暗黑.血量倍率());
        结果.怪物变色 = 怪物变异配置.暗黑.颜色;
    } else if (几率 >= 100 && 几率 <= 200) {
        // 噩梦变异 (100-200)
        结果.怪物后缀 = 怪物变异配置.噩梦.后缀;
        结果.攻击倍率 = String(怪物变异配置.噩梦.攻击倍率());
        结果.血量倍率 = String(怪物变异配置.噩梦.血量倍率());
        结果.怪物变色 = 怪物变异配置.噩梦.颜色;
    }
    
    return 结果;
}

/**
 * 计算怪物星级
 * @param 取尾数 怪物标签尾数
 * @param 匹配配置 怪物配置
 * @param 当前地图名 地图名称
 * @param 星星增加 星级增加值
 * @param 地图星级 地图星级
 * @returns 计算后的星级字符串
 */
function 计算怪物星级(取尾数: number, 匹配配置: 刷怪配置, 当前地图名: string, 星星增加: number, 地图星级: string): string {
    // 获取当前地图配置信息
    const 副本池 = 地图.取地图();
    const 当前地图配置 = 副本池.find(config => config.地图名 === 当前地图名);
    
    let 地图挑战星级: string;
    
    // console.log(`[星级计算] 地图: ${当前地图名}, 地图星级: ${地图星级}`);
    
    if (当前地图配置) {
        // 根据地图星级是否等于固定星级来判断地图类型
        const 固定星级 = 当前地图配置.固定星级;
        const 当前星级 = parseInt(地图星级);
        const 是固定星级地图 = 当前星级 === 固定星级;
        
        // console.log(`[星级计算] 固定星级: ${固定星级}, 当前星级: ${当前星级}, 是固定星级地图: ${是固定星级地图}`);
        
        // 如果地图星级等于固定星级，说明是固定星级地图
        if (是固定星级地图) {
            // 固定星级地图：地图星级 * 10
            地图挑战星级 = js_number(地图星级, `10`, 3);
            // console.log(`[星级计算] 固定星级地图: ${地图星级} * 10 = ${地图挑战星级}`);
        } else {
            // 其余的均为挑战倍数地图：基础倍率10000，每1000星级增加100倍率
            const 基础倍率 = 10000;
            const 星级加成 = Math.floor(当前星级 / 500) * 10 + 100;
            const 最终倍率 = 基础倍率 * 星级加成;
            
            地图挑战星级 = js_number(地图星级, `${String(最终倍率)}`, 3);
            // console.log(`[星级计算] 挑战地图: ${地图星级} * ${最终倍率} (基础${基础倍率} + 星级加成${星级加成}) = ${地图挑战星级}`);
        }
    } else {
        // 默认情况：使用原来的逻辑
        地图挑战星级 = js_number(地图星级, `10`, 3);
        // console.log(`[星级计算] 未找到配置，默认: ${地图星级} * 10 = ${地图挑战星级}`);
    }
    
    // 怪物星星 = 地图挑战星级 * 怪物星星大 * 大数值刷怪配置中的地图星星
    const 大数值调整 = js_number(大数值刷怪配置[当前地图名].星星, `2`, 3);
    const 最终星级 = js_number(
        js_number(地图挑战星级, 匹配配置.怪物星星大, 3),
        大数值调整,
        3
    );
    
    // console.log(`[星级计算] 大数值调整: ${大数值调整}, 怪物星星大: ${匹配配置.怪物星星大}`);
    // console.log(`[星级计算] 最终计算: (${地图挑战星级} * ${匹配配置.怪物星星大}) * ${大数值调整} = ${最终星级}`);

    return 最终星级;
}

