// 自动计算星星数值的函数
function 计算地图星星数值(地图索引: number): string {
    let 当前数值 = '1'; // 从1开始

    
    for (let i = 1; i < 地图索引; i++) {
        let 大陆倍数: number;
        const 区间档 = Math.ceil(i / 5); 
        switch (区间档) {
            case 1: // i ≤5（1-5级）
                大陆倍数 = 50;
                break;
            case 2: // i ≤10（6-10级）
                大陆倍数 = 5000;
                break;
            case 3: // i ≤15（11-15级）
                大陆倍数 = 30000;
                break;
            case 4: // i ≤20（16-20级）
                大陆倍数 = 200000000;
                break;
            case 5: // i ≤25（21-25级）
                大陆倍数 = 5000000;
                break;
            case 6: // i ≤30（26-30级）
                大陆倍数 = 1000000000;
                break;
            case 7: // i ≤35（31-35级）
                大陆倍数 = 100000000000;
                break;
            case 8: // i ≤40（36-40级）
                大陆倍数 = 100000000000000;
                break;
            case 9: // i ≤45（41-45级）
                大陆倍数 = 1000000000000000;
                break;
            case 10: // i ≤50（46-50级）
                大陆倍数 = 100000000000000000;
                break;
            default: // i >15（16级及以上，每多1档=多5级，倍数+10000）
                // 公式：基础倍数（27000） + (当前档 - 3) * 10000（档数差×递增幅度）
                大陆倍数 = 1000000000 + (区间档 - 3) * 10000;
                break;
        }
        
        // 使用大数值计算库进行乘法 (mode 3 = 乘法)
        当前数值 = js_number(当前数值, 大陆倍数.toString(), 3);
    }

    // 地图倍数计算：20级后开始，每个地图增加倍数
    for (let i = 1; i < 地图索引; i++) {
        let 地图倍数: number = 1; // 默认倍数为1（不变）
        
        if (i > 20) { // 20级后开始增加地图倍数
            if (i <= 25) {
                地图倍数 = 1; // 21-25级
            } else if (i <= 30)  {
                地图倍数 = 20; // 26级及以上
            } else if (i <= 35) {
                地图倍数 = 2500; // 31级及以上
            } else if (i <= 40) {
                地图倍数 = 5000; // 41级及以上
            } else if (i <= 45) {
                地图倍数 = 10000; // 46级及以上
            } else if (i <= 50) {
                地图倍数 = 20000; // 51级及以上
            } else {
                地图倍数 = 100000000000; // 26级及以上
            }
            
            // 使用大数值计算库进行乘法 (mode 3 = 乘法)
            当前数值 = js_number(当前数值, 地图倍数.toString(), 3);
        }
    }
    return 当前数值;
}

