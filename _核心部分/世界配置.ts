/** 获取地图的生物名字 */
export function 取生物名字(地图名: string, TAG尾数: number): string {
    // 优先使用完整配置
    const 完整配置 = 取完整地图配置(地图名)
    if (完整配置?.生物名字) {
        // TAG尾数 1-5 对应数组索引 0-4
        const 索引 = TAG尾数 - 1
        if (索引 >= 0 && 索引 < 完整配置.生物名字.length) {
            return 完整配置.生物名字[索引]
        }
    }

    // 默认名字
    const 类型名 = ['', '小怪', '精英', '稀有', '精英首领', '首领', '大陆BOSS', '特殊BOSS']
    return `${地图名}${类型名[TAG尾数] || '怪物'}`
}

