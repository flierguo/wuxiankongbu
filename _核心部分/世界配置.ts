/**
 * ============================================================================
 * 世界配置 - 统一管理大陆、地图、生物、装备配置
 * ============================================================================
 * 
 * 【核心设计理念】
 * 地图星级 → 生物属性 → 装备属性 三者紧密关联
 * 只需调整核心配置，所有数值自动联动更新
 * 
 * 【核心公式】
 * 1. 星级倍数 = 根据固定星级，按星级区间幂运算递增（5倍/10倍段）
 * 2. 生物属性 = 生物基础值 × 星级倍数(幂运算) × TAG倍数 × 难度系数(普通乘法)
 *    - 星级倍数：由固定星级决定，大地图之间的指数级差距
 *    - 难度系数：同一大地图内不同难度的线性倍数（简单1/普通5/困难25...）
 * 3. 装备属性 = 装备品质基础 × 星级倍数 × 随机倍数 × 极品倍率 × 神器倍数 × 怪物装备倍数
 *    - 装备属性只与固定星级有关，不受难度系数影响
 * 
 * 【设计原则】
 * 1. 星级驱动：地图固定星级是核心常量，所有数值基于星级计算
 * 2. 分段递增：不同星级区间可配置不同递增倍数（5倍/10倍）
 * 3. 品质关联：装备品质决定基础数值，与星级倍数相乘得到最终属性
 * 4. 性能优先：减少大数值乘法次数，降低CPU消耗
 * 
 * 【快速调整指南】
 * - 调整难度递增速度：修改 星级递增配置
 * - 调整怪物类型差异：修改 TAG倍数配置
 * - 调整副本难度差异：修改 难度系数配置
 * - 调整装备品质差异：修改 装备品质配置
 * - 调整基础数值：修改 生物基础值配置 / 装备基础值配置
 * 
 * ============================================================================
 */

